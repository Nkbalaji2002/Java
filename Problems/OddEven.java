// Find the Even or Odd numbers
package Problems;

public class OddEven {
    public static void main(String[] args) {
        int number = 5;
        number = 10;

        if (number % 2 == 0) {
            System.out.println(number + " is Even number.");
        } else {
            System.out.println(number + " is Odd number.");
        }
    }
}
