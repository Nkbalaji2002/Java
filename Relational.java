public class Relational {
    public static void main(String[] args) {
        int x = 6, y =  6;
        boolean result;

        result = x > y;
        System.out.println(result);

        result = x < y;
        System.out.println(result);

        result = (x == y);
        System.out.println(result);

        result = (x != y);
        System.out.println(result);
        
    }
}
