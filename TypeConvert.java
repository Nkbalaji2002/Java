public class TypeConvert {
    public static void main(String[] args) {
        // int to double
        int a = 59;
        double b = a;

        System.out.println("a : " + a);
        System.out.println("b : " + b);

        // double to int
        double c = 49.39;
        int d = (int) c;

        System.out.println("c : " + c);
        System.out.println("d : " + d);

    }
}
